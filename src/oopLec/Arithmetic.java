package oopLec;

public class Arithmetic {

   public static final double PI = 3.14159;

   public static int add(int x, int y) {
       return x + y;
   }

   public static int multiply(int x, int y){
       return x * y;
   }

//    static means we can use the class, property and method to access in main
//    Static things are a great way to create a bag of tools...we refer to the "bag" by class name
//    otherwise we would have to instantiate a new arithmetic object ( have to use name of the object)
}
